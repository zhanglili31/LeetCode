    class Solution {
        public boolean searchMatrix(int[][] matrix, int target) {
            int m = matrix.length;
            int n = matrix[0].length;
            int left = 0;
            int right = m * n - 1;
            while (left <= right) {
                int mid = left + (right - left) / 2;
                int x = mid / n;
                int y = mid % n;
                int val = matrix[x][y];
                if (target == val) {
                    return true;
                } else if (val < target) {
                    left = mid + 1;
                } else {
                    right = mid - 1;
                }
            }
            return false;

        }
    }
