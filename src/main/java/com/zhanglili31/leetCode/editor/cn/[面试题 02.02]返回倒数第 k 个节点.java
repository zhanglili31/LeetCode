    /**
     * Definition for singly-linked list.
     * public class ListNode {
     * int val;
     * ListNode next;
     * ListNode(int x) { val = x; }
     * }
     */
    class Solution {
        public int kthToLast(ListNode head, int k) {
            ListNode res = head;
            ListNode next = head;
            int distance = 1;
            while (distance < k) {
                distance++;
                next = next.next;
            }
            while (next.next != null) {
                res = res.next;
                next = next.next;
            }
            return res.val;

        }
    }
